    <h4> Sejarah hari guru tidak bisa dilepaskan dari keberadaan organisasi PGRI. Melansir situs resmi PGRI, organisasi ini lahir pada tahun 1912.
    Awalnya bernama Persatuan Guru Hindia Belanda (PGHB) dengan anggota guru bantu, guru desa, kepala sekolah, sampai pemilik sekolah dari latar pendidikan yang berbeda-beda.
    Setelah lahir PGHB, disusul kemudian kelahiran organisasi-organisasi guru lain di berbagai daerah. Pada 1932, 32 organisasi guru sepakat bersatu dengan nama Persatuan Guru Indonesia (PGI).
    Pergerakan PGI sempat terhenti pada masa penjajahan Jepang. Pemerintah Negeri Sakura melarang keberadaan organisasi dan menutup sekolah.
    Setelah proklamasi kemerdekaan, diadakan Kongres Guru Indonesia pada tanggal 24-25 November 1945 di Surakarta. Di dalam kongres inilah, pada tanggal 25 November 1945, PGRI berdiri dengan tiga tujuan :
   <h4 class="font-italic font-weight-bold"> 1. Mempertahankan dan menyempurnakan Republik Indonesia.</h4>
   <h4 class="font-italic font-weight-bold"> 2. Mempertinggi tingkat pendidikan dan pengajaran sesuai dengan dasar-dasar kerakyatan.</h4>
   <h4 class="font-italic font-weight-bold"> 3. Membela hak dan nasib buruh umumnya, guru pada khususnya. </h4>
   <h4> Situasi politik yang dinamis tidak menyurutkan semangat PGRI untuk tetap setia dalam pengabdiannya sebagai organisasi perjuangan, organisasi profesi, dan organisasi ketenagakerjaan, yang bersifat unitaristik, dan independen.
    Oleh karena itu sebagai bentuk penghormatan kepada guru, pemerintah Republik Indonesia menetapkan 25 November sebagai Hari Guru Nasional. </h4>
